// Helper functions
export function getProperty(id: string): Property | undefined {
  return properties.find((p) => p.id === id);
}

export function getPropertyByShareId(shareId: string): Property | undefined {
  return properties.find((p) => p.handbook.shareId === shareId);
}

export function getRoom(propertyId: string, roomId: string) {
  const property = getProperty(propertyId);
  return property?.rooms.find((r) => r.id === roomId);
}

export function getAppliance(propertyId: string, applianceId: string) {
  const property = getProperty(propertyId);
  return property?.appliances.find((a) => a.id === applianceId);
}

export function getAppliancesForRoom(propertyId: string, roomId: string) {
  const property = getProperty(propertyId);
  if (!property) return [];
  return property.appliances.filter((a) => a.roomId === roomId);
}

export function getRoomForAppliance(propertyId: string, applianceId: string) {
  const property = getProperty(propertyId);
  if (!property) return undefined;
  const appliance = property.appliances.find((a) => a.id === applianceId);
  if (!appliance) return undefined;
  return property.rooms.find((r) => r.id === appliance.roomId);
}

export function getRecentActivities(limit = 10): Activity[] {
  return [...activities]
    .sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime())
    .slice(0, limit);
}

export function getActivitiesForProperty(propertyId: string): Activity[] {
  return activities
    .filter((a) => a.propertyId === propertyId)
    .sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime());
}
